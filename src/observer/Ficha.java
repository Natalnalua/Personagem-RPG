package observer;

public class Ficha {
        private int numFicha;
	private String nome;
	private String classe;
	private String raca;
        private String arma;
        private String bota;
        private String calca;
        private String peitoral;
        private String elmo;

	
	public int getNumFicha() {
            return numFicha;
	}
	public void setNumFicha(int numEdicao) {
            this.numFicha = numEdicao;
	}
	public String getNome() {
            return nome;
	}
	public void setNome(String nome) {
            this.nome = nome;
	}
	public String getClasse() {
            return classe;
	}
	public void setClasse(String classe) {
            this.classe = classe;
	}
	public String getRaca() {
            return raca;
	}
	public void setRaca(String nome) {
            this.raca = nome;
	}
        public String getArma() {
            return arma;
	}
	public void setArma(String arma) {
            this.arma = arma;
	}      
        public String getBota() {
            return bota;
	}
	public void setBota(String bota) {
            this.bota = bota;
	}
        public String getCalca() {
            return calca;
	}
	public void setCalca(String calca) {
            this.calca = calca;
	}
        public String getPeitoral() {
            return peitoral;
	}
	public void setPeitoral(String peitoral) {
            this.peitoral = peitoral;
	}
        public String getElmo() {
            return elmo;
	}
	public void setElmo(String elmo) {
            this.elmo = elmo;
	}
}
